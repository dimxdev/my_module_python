#casting tipe data adalah merubah tibe data satu ke tipe data yang lain

#integer
print("==== integer ====")
dataInteger = 9
dataFloat = float(dataInteger)
dataString = str(dataInteger)
dataBool = bool(dataInteger)
print("data:", dataInteger, "  ", "tipe data:", type(dataInteger))
print("data:", dataFloat, "  ", "tipe data:", type(dataFloat))
print("data:", dataString, "  ", "tipe data:", type(dataString))
print("data:", dataBool, "  ", "tipe data:", type(dataBool), "\n")


